# =============================================================================
# Import packages
# =============================================================================
import numpy as np
import matplotlib.pyplot as plt

# =============================================================================
# Parameters
# =============================================================================
file_in = 'midterm_dydx.txt'

# =============================================================================
# load data
# =============================================================================
mData = np.loadtxt(file_in).T

# =============================================================================
# calculation
# =============================================================================
z = mData[0]
f_z = mData[1]
delta_z = z[1::] - z[0:-1]

dfdz = (f_z[2::] - f_z[0:-2])/(2*delta_z[1::])

df2d2z = (f_z[2::] - 2*f_z[1:-1] + f_z[0:-2])/(delta_z[1::])**2

# =============================================================================
# plotting
# =============================================================================
fig, (ax1, ax2, ax3) = plt.subplots( 3, 1, sharex = True, figsize = (10,12))
fig.subplots_adjust(hspace = 0.2)
plt.rcParams.update({'font.size': 22})

ax1.plot( z[0::], f_z, 'b-')
ax1.set(ylabel = 'f(x)')

ax2.plot(z[1:-1], dfdz, 'r-')
ax2.set(ylabel = "f'(x)")

ax3.plot(z[1:-1], df2d2z, 'g-')
ax3.set(ylabel = "f''(x)")

plt.savefig('Problem 5 subplots.png')






















