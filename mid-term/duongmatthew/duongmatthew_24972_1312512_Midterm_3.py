import numpy as np
import matplotlib.pyplot as plt

#===================================================================================
#                                params, files
#===================================================================================
file_in = 'midterm_dydx.txt'
mData = np.genfromtxt(file_in).T

#===================================================================================
#                                calculations
#===================================================================================
dt = mData[0][2::] - mData[0][0:-2]
v = (mData[1][2::] - mData[1][0:-2])/dt
a = (v[2::] - v[0:-2])/dt[1:-1]

#===================================================================================
#                                plots
#===================================================================================
plt.figure(1)

ax1 = plt.subplot(311)
ax1.plot(mData[0], mData[1], 'k-')

dx = plt.subplot(312)
dx.plot(mData[0][1:-1], v, 'b-')

dx2 = plt.subplot(313)
dx2.plot(mData[0][2:-2], a, 'r-')

plt.show()